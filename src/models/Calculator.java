package models;

public class Calculator {

    public int sum(int num1, int num2){
        return num1+num2;
    }

    public int subtraction(int num1, int num2){
        return num1-num2;
    }

    public int multiplication(int num1, int num2){
        return num1*num2;
    }

    public double split(int num1, int num2){
        return num1/num2;
    }
}